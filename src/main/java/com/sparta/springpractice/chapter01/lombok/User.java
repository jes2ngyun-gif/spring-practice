package com.sparta.springpractice.chapter01.lombok;

import lombok.ToString;

@ToString
public class User {

    private String name;
    private int age;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
